#!/usr/bin/env bash
# copy-config.sh - Generate .sidekick/sidekick.config with verbose comments
# See docs/plans/2025-12-27-copy-config-script-design.md for design details

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
DEFAULTS_DIR="$PROJECT_ROOT/assets/sidekick/defaults"

# Default output path
OUTPUT_PATH=".sidekick/sidekick.config"

# Section flags (0 = exclude, 1 = include)
INCLUDE_CORE=0
INCLUDE_LLM=0
INCLUDE_TRANSCRIPT=0
INCLUDE_REMINDERS=0
INCLUDE_STATUSLINE=0
INCLUDE_SESSION_SUMMARY=0

# ------------------------------------------------------------------------------
# Helper functions
# ------------------------------------------------------------------------------

usage() {
    cat <<EOF
Usage: $(basename "$0") [OPTIONS]

Generate a .sidekick/sidekick.config file with verbose comments.

OPTIONS:
    --all                       Include all sections
    --core                      Include core settings (logging, paths, daemon, ipc)
    --llm                       Include LLM provider settings
    --transcript                Include transcript processing settings
    --features.reminders        Include reminders feature settings
    --features.statusline       Include statusline feature settings
    --features.session-summary  Include session summary feature settings
    --output <path>             Output path (default: .sidekick/sidekick.config)
    --help                      Show this help message

EXAMPLES:
    $(basename "$0") --all
    $(basename "$0") --core --llm
    $(basename "$0") --features.reminders --features.statusline
    $(basename "$0") --all --output ~/.sidekick/sidekick.config
EOF
}

die() {
    echo "Error: $1" >&2
    exit 1
}

check_yq() {
    if ! command -v yq &>/dev/null; then
        cat >&2 <<EOF
Error: yq is required but not installed.

Install yq:
  macOS:   brew install yq
  Linux:   sudo snap install yq
           or download from https://github.com/mikefarah/yq/releases

EOF
        exit 1
    fi
}

# Read a YAML value with yq, return empty string if not found
yaml_get() {
    local file="$1"
    local path="$2"
    yq -r "$path // \"\"" "$file" 2>/dev/null || echo ""
}

# ------------------------------------------------------------------------------
# Section generators
# Each function outputs config lines for its section
# ------------------------------------------------------------------------------

emit_header() {
    cat <<'EOF'
# ==============================================================================
# Sidekick Configuration
# Generated by: scripts/copy-config.sh
# Override values as needed - see docs/design/CONFIG-SYSTEM.md for details
# ==============================================================================

EOF
}

emit_core_section() {
    local file="$DEFAULTS_DIR/core.defaults.yaml"
    [[ -f "$file" ]] || die "Core defaults not found: $file"

    cat <<'EOF'
# ------------------------------------------------------------------------------
# Core Settings
# Logging, paths, daemon process, and IPC configuration
# ------------------------------------------------------------------------------

# Log level: debug, info, warn, error
# debug = all messages including internal state
# info = normal operation messages
# warn = potential issues that don't prevent operation
# error = failures and critical issues only
EOF
    echo "core.logging.level=$(yaml_get "$file" '.logging.level')"
    echo ""

    cat <<'EOF'
# Output format: pretty (human-readable) or json (structured for parsing)
EOF
    echo "core.logging.format=$(yaml_get "$file" '.logging.format')"
    echo ""

    cat <<'EOF'
# Enable console output in addition to file logging
# Set to true for debugging, false for normal operation
EOF
    echo "core.logging.consoleEnabled=$(yaml_get "$file" '.logging.consoleEnabled')"
    echo ""

    cat <<'EOF'
# State directory relative to project root
# Stores session data, logs, staged reminders, etc.
EOF
    echo "core.paths.state=$(yaml_get "$file" '.paths.state')"
    echo ""

    cat <<'EOF'
# Custom assets directory path (optional)
# Omit or leave empty to use default bundled assets
# core.paths.assets=/path/to/custom/assets

# Time in milliseconds before idle daemon shuts down
# Default: 300000 (5 minutes)
EOF
    echo "core.daemon.idleTimeoutMs=$(yaml_get "$file" '.daemon.idleTimeoutMs')"
    echo ""

    cat <<'EOF'
# Graceful shutdown timeout in milliseconds
# Time allowed for in-flight tasks to complete during shutdown
# Default: 30000 (30 seconds)
EOF
    echo "core.daemon.shutdownTimeoutMs=$(yaml_get "$file" '.daemon.shutdownTimeoutMs')"
    echo ""

    cat <<'EOF'
# IPC connection timeout in milliseconds
# How long to wait when connecting to daemon
EOF
    echo "core.ipc.connectTimeoutMs=$(yaml_get "$file" '.ipc.connectTimeoutMs')"
    echo ""

    cat <<'EOF'
# IPC request timeout in milliseconds
# How long to wait for daemon responses
EOF
    echo "core.ipc.requestTimeoutMs=$(yaml_get "$file" '.ipc.requestTimeoutMs')"
    echo ""

    cat <<'EOF'
# Maximum retry attempts for failed IPC connections
EOF
    echo "core.ipc.maxRetries=$(yaml_get "$file" '.ipc.maxRetries')"
    echo ""

    cat <<'EOF'
# Delay between IPC connection retries in milliseconds
EOF
    echo "core.ipc.retryDelayMs=$(yaml_get "$file" '.ipc.retryDelayMs')"
    echo ""
}

emit_llm_section() {
    local file="$DEFAULTS_DIR/llm.defaults.yaml"
    [[ -f "$file" ]] || die "LLM defaults not found: $file"

    cat <<'EOF'
# ------------------------------------------------------------------------------
# LLM Settings
# Language model provider configuration for Sidekick features
# API keys should be stored in .env files, not here
# ------------------------------------------------------------------------------

# LLM provider: claude-cli, openai, openrouter, custom, emulator
# claude-cli = use Claude Code's built-in model (no API key needed)
# openai = OpenAI API (requires OPENAI_API_KEY in .env)
# openrouter = OpenRouter API (requires OPENROUTER_API_KEY in .env)
# custom = custom endpoint (configure via environment)
# emulator = testing emulator (for development)
EOF
    echo "llm.provider=$(yaml_get "$file" '.provider')"
    echo ""

    cat <<'EOF'
# Model identifier (provider-specific)
# Omit to use provider's default model
# Examples:
#   openai: gpt-4o, gpt-4o-mini
#   openrouter: anthropic/claude-3.5-sonnet, openai/gpt-4o
# llm.model=anthropic/claude-3.5-sonnet

# Sampling temperature (0-1)
# 0 = deterministic, consistent responses
# 1 = creative, varied responses
EOF
    echo "llm.temperature=$(yaml_get "$file" '.temperature')"
    echo ""

    cat <<'EOF'
# Maximum tokens to generate (optional)
# Omit to use provider's default
# llm.maxTokens=4096

# Fallback provider if primary fails after retries
# Omit to disable fallback
# llm.fallbackProvider=openai

# Fallback model if primary fails
# llm.fallbackModel=gpt-4o

# Request timeout in seconds (1-300)
EOF
    echo "llm.timeout=$(yaml_get "$file" '.timeout')"
    echo ""

    cat <<'EOF'
# Maximum retries on timeout before fallback (0-10)
EOF
    echo "llm.timeoutMaxRetries=$(yaml_get "$file" '.timeoutMaxRetries')"
    echo ""

    cat <<'EOF'
# Enable debug request/response dumps to logs
# Useful for debugging LLM integration issues
EOF
    echo "llm.debugDumpEnabled=$(yaml_get "$file" '.debugDumpEnabled')"
    echo ""
}

emit_transcript_section() {
    local file="$DEFAULTS_DIR/transcript.defaults.yaml"
    [[ -f "$file" ]] || die "Transcript defaults not found: $file"

    cat <<'EOF'
# ------------------------------------------------------------------------------
# Transcript Settings
# Transcript file watching and metrics persistence
# ------------------------------------------------------------------------------

# Debounce time in milliseconds for transcript file watch events
# Prevents excessive processing during rapid file updates
# Lower = more responsive, Higher = less CPU usage
EOF
    echo "transcript.watchDebounceMs=$(yaml_get "$file" '.watchDebounceMs')"
    echo ""

    cat <<'EOF'
# Interval in milliseconds between metrics persistence writes
# Lower = more frequent disk writes (safer)
# Higher = potential data loss on crash (more efficient)
EOF
    echo "transcript.metricsPersistIntervalMs=$(yaml_get "$file" '.metricsPersistIntervalMs')"
    echo ""
}

emit_reminders_section() {
    local file="$DEFAULTS_DIR/features/reminders.defaults.yaml"
    [[ -f "$file" ]] || die "Reminders defaults not found: $file"

    cat <<'EOF'
# ------------------------------------------------------------------------------
# Reminders Feature
# Automatic reminders when Claude has been working without user input
# ------------------------------------------------------------------------------

# Enable or disable the reminders feature
EOF
    echo "features.reminders.enabled=$(yaml_get "$file" '.enabled')"
    echo ""

    cat <<'EOF'
# Tool calls per turn that trigger "time for user update" reminder
# Counts tool invocations since the last user prompt
# Fires when: update_threshold <= toolsThisTurn < stuck_threshold
EOF
    echo "features.reminders.update_threshold=$(yaml_get "$file" '.update_threshold')"
    echo ""

    cat <<'EOF'
# Tool calls per turn that trigger "are you stuck" reminder
# Takes priority over update_threshold
# Fires when: toolsThisTurn >= stuck_threshold
EOF
    echo "features.reminders.stuck_threshold=$(yaml_get "$file" '.stuck_threshold')"
    echo ""
}

emit_statusline_section() {
    local file="$DEFAULTS_DIR/features/statusline.defaults.yaml"
    [[ -f "$file" ]] || die "Statusline defaults not found: $file"

    cat <<'EOF'
# ------------------------------------------------------------------------------
# Statusline Feature
# Custom status line display in Claude Code
# ------------------------------------------------------------------------------

# Enable or disable the statusline feature
EOF
    echo "features.statusline.enabled=$(yaml_get "$file" '.enabled')"
    echo ""

    cat <<'EOF'
# Format string for statusline output
# Available placeholders:
#   {model}    - Current model name
#   {tokens}   - Token usage count
#   {cost}     - Estimated cost
#   {duration} - Session duration
#   {cwd}      - Current working directory
#   {branch}   - Git branch name
#   {summary}  - Session summary
#   {title}    - Session title
EOF
    echo "features.statusline.format=$(yaml_get "$file" '.format')"
    echo ""

    cat <<'EOF'
# Confidence threshold for preferring session summary over first-prompt summary
# Range: 0.0-1.0
EOF
    echo "features.statusline.confidenceThreshold=$(yaml_get "$file" '.confidenceThreshold')"
    echo ""

    cat <<'EOF'
# Token count at which to show warning color
EOF
    echo "features.statusline.thresholds.tokens.warning=$(yaml_get "$file" '.thresholds.tokens.warning')"
    echo ""

    cat <<'EOF'
# Token count at which to show critical color
EOF
    echo "features.statusline.thresholds.tokens.critical=$(yaml_get "$file" '.thresholds.tokens.critical')"
    echo ""

    cat <<'EOF'
# Cost in USD at which to show warning color
EOF
    echo "features.statusline.thresholds.cost.warning=$(yaml_get "$file" '.thresholds.cost.warning')"
    echo ""

    cat <<'EOF'
# Cost in USD at which to show critical color
EOF
    echo "features.statusline.thresholds.cost.critical=$(yaml_get "$file" '.thresholds.cost.critical')"
    echo ""

    cat <<'EOF'
# Use Nerd Font icons for enhanced display
# Requires a Nerd Font installed in your terminal
EOF
    echo "features.statusline.theme.useNerdFonts=$(yaml_get "$file" '.theme.useNerdFonts')"
    echo ""

    cat <<'EOF'
# Color assignments for statusline elements
# Available colors: black, red, green, yellow, blue, magenta, cyan, white
EOF
    echo "features.statusline.theme.colors.model=$(yaml_get "$file" '.theme.colors.model')"
    echo "features.statusline.theme.colors.tokens=$(yaml_get "$file" '.theme.colors.tokens')"
    echo "features.statusline.theme.colors.summary=$(yaml_get "$file" '.theme.colors.summary')"
    echo ""
}

emit_session_summary_section() {
    local file="$DEFAULTS_DIR/features/session-summary.defaults.yaml"
    [[ -f "$file" ]] || die "Session summary defaults not found: $file"

    cat <<'EOF'
# ------------------------------------------------------------------------------
# Session Summary Feature
# Automatic session summarization using LLM
# ------------------------------------------------------------------------------

# Enable or disable the session summary feature
EOF
    echo "features.session-summary.enabled=$(yaml_get "$file" '.enabled')"
    echo ""

    cat <<'EOF'
# Number of transcript lines to include in excerpt for LLM analysis
# Higher = more context, more tokens used
EOF
    echo "features.session-summary.excerptLines=$(yaml_get "$file" '.excerptLines')"
    echo ""

    cat <<'EOF'
# Filter out tool use messages from excerpt
# Reduces noise and token usage
EOF
    echo "features.session-summary.filterToolMessages=$(yaml_get "$file" '.filterToolMessages')"
    echo ""

    cat <<'EOF'
# Keep summary history across context compactions
EOF
    echo "features.session-summary.keepHistory=$(yaml_get "$file" '.keepHistory')"
    echo ""

    cat <<'EOF'
# Maximum words for generated session title
EOF
    echo "features.session-summary.maxTitleWords=$(yaml_get "$file" '.maxTitleWords')"
    echo ""

    cat <<'EOF'
# Maximum words for generated intent description
EOF
    echo "features.session-summary.maxIntentWords=$(yaml_get "$file" '.maxIntentWords')"
    echo ""

    cat <<'EOF'
# Enable snarky/humorous comments in summaries
EOF
    echo "features.session-summary.snarkyMessages=$(yaml_get "$file" '.snarkyMessages')"
    echo ""

    cat <<'EOF'
# Countdown configuration for summary refresh timing
# Counts ToolResult events (each tool use decrements by 1)
# Lower values = more frequent updates (more LLM calls)

# Tool results until next summary when confidence is low (<=0.6)
EOF
    echo "features.session-summary.countdown.lowConfidence=$(yaml_get "$file" '.countdown.lowConfidence')"
    echo ""

    cat <<'EOF'
# Tool results until next summary when confidence is medium (0.6-0.8)
EOF
    echo "features.session-summary.countdown.mediumConfidence=$(yaml_get "$file" '.countdown.mediumConfidence')"
    echo ""

    cat <<'EOF'
# Tool results until next summary when confidence is high (>0.8)
# Set very high to effectively disable automatic refresh
EOF
    echo "features.session-summary.countdown.highConfidence=$(yaml_get "$file" '.countdown.highConfidence')"
    echo ""

    cat <<'EOF'
# Minimum confidence required to create a bookmark
EOF
    echo "features.session-summary.bookmark.confidenceThreshold=$(yaml_get "$file" '.bookmark.confidenceThreshold')"
    echo ""

    cat <<'EOF'
# Confidence drop below which bookmark resets
EOF
    echo "features.session-summary.bookmark.resetThreshold=$(yaml_get "$file" '.bookmark.resetThreshold')"
    echo ""
}

# ------------------------------------------------------------------------------
# Main
# ------------------------------------------------------------------------------

parse_args() {
    while [[ $# -gt 0 ]]; do
        case "$1" in
            --all)
                INCLUDE_CORE=1
                INCLUDE_LLM=1
                INCLUDE_TRANSCRIPT=1
                INCLUDE_REMINDERS=1
                INCLUDE_STATUSLINE=1
                INCLUDE_SESSION_SUMMARY=1
                shift
                ;;
            --core)
                INCLUDE_CORE=1
                shift
                ;;
            --llm)
                INCLUDE_LLM=1
                shift
                ;;
            --transcript)
                INCLUDE_TRANSCRIPT=1
                shift
                ;;
            --features.reminders)
                INCLUDE_REMINDERS=1
                shift
                ;;
            --features.statusline)
                INCLUDE_STATUSLINE=1
                shift
                ;;
            --features.session-summary)
                INCLUDE_SESSION_SUMMARY=1
                shift
                ;;
            --output)
                [[ -n "${2:-}" ]] || die "--output requires a path argument"
                OUTPUT_PATH="$2"
                shift 2
                ;;
            --help|-h)
                usage
                exit 0
                ;;
            *)
                die "Unknown option: $1 (use --help for usage)"
                ;;
        esac
    done

    # If no sections specified, show usage
    local any_selected=$((INCLUDE_CORE + INCLUDE_LLM + INCLUDE_TRANSCRIPT + INCLUDE_REMINDERS + INCLUDE_STATUSLINE + INCLUDE_SESSION_SUMMARY))
    if [[ $any_selected -eq 0 ]]; then
        echo "No sections specified. Use --all or specify individual sections."
        echo ""
        usage
        exit 1
    fi
}

check_output_path() {
    local dir
    dir="$(dirname "$OUTPUT_PATH")"

    # Create directory if needed
    if [[ ! -d "$dir" ]]; then
        echo "Creating directory: $dir"
        mkdir -p "$dir"
    fi

    # Check for existing file
    if [[ -f "$OUTPUT_PATH" ]]; then
        read -r -p "File exists: $OUTPUT_PATH. Overwrite? [y/N] " response
        case "$response" in
            [yY]|[yY][eE][sS])
                echo "Overwriting..."
                ;;
            *)
                echo "Aborted."
                exit 0
                ;;
        esac
    fi
}

generate_config() {
    {
        emit_header

        [[ $INCLUDE_CORE -eq 1 ]] && emit_core_section
        [[ $INCLUDE_LLM -eq 1 ]] && emit_llm_section
        [[ $INCLUDE_TRANSCRIPT -eq 1 ]] && emit_transcript_section
        [[ $INCLUDE_REMINDERS -eq 1 ]] && emit_reminders_section
        [[ $INCLUDE_STATUSLINE -eq 1 ]] && emit_statusline_section
        [[ $INCLUDE_SESSION_SUMMARY -eq 1 ]] && emit_session_summary_section
    } > "$OUTPUT_PATH"

    echo "Generated: $OUTPUT_PATH"
}

main() {
    parse_args "$@"
    check_yq
    check_output_path
    generate_config
}

main "$@"
